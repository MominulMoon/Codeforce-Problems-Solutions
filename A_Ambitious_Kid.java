import java.io.*;
import java.util.*;

public class A_Ambitious_Kid {
    static BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
    static StringTokenizer st = new StringTokenizer("");
    static PrlongWriter out = new PrlongWriter(System.out);

    void solve() throws IOException {
        long n=nextlong();

        // long []arr=new long[n];
        long mn = longeger.MAX_VALUE;
        for(long i=0;i<n;i++)
        {
            long k=nextlong();
            k=Math.abs(k);
            mn=Math.min(mn,k);

        }
        System.out.prlongln(mn);

    }

    public static void main(String[] args) throws IOException {
        long t = 1;
        //t = nextlong(); // Uncomment for multiple test cases
        while (t-- > 0) {
            new A_Ambitious_Kid().solve();
        }
        out.close();
    }

    static String next() throws IOException {
        while (!st.hasMoreTokens()) st = new StringTokenizer(br.readLine());
        return st.nextToken();
    }

    static long nextlong() throws IOException { return longeger.parselong(next()); }
    static long nextLong() throws IOException { return Long.parseLong(next()); }
    static double nextDouble() throws IOException { return Double.parseDouble(next()); }

    static long gcd(long a, long b) { return b == 0 ? a : gcd(b, a % b); }
    static long gcd(long a, long b) { return b == 0 ? a : gcd(b, a % b); }
    static long lcm(long a, long b) { return a / gcd(a, b) * b; }

    static long binarySearch(long[] arr, long key) {
        long low = 0, high = arr.length - 1;
        while (low <= high) {
            long mid = (low + high) >>> 1;
            if (arr[mid] == key) return mid;
            else if (arr[mid] < key) low = mid + 1;
            else high = mid - 1;
        }
        return -1;
    }

    static void sort(long[] arr) {
        Arrays.sort(arr);
    }

    static void sort(long[] arr) {
        Arrays.sort(arr);
    }

    static long[] readlongArray(long n) throws IOException {
        long[] a = new long[n];
        for (long i = 0; i < n; i++) a[i] = nextlong();
        return a;
    }

    static long[] readLongArray(long n) throws IOException {
        long[] a = new long[n];
        for (long i = 0; i < n; i++) a[i] = nextLong();
        return a;
    }
}

